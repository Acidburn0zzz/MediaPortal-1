using System;
using System.Collections.Generic;
using Gentle.Framework;
using RuleBasedScheduler;
using TvLibrary.Interfaces;
using TvLibrary.Log;

namespace TvDatabase
{
  /// <summary>
  /// Instances of this class represent the properties and methods of a row in the table <b>ScheduleRulesTemplate</b>.
  /// </summary>
  [TableName("ScheduleRulesTemplate")]
  public class ScheduleRulesTemplate : Persistent
  {
    #region Members

    private ScheduleConditionList _scheduleConditions;

    private bool isChanged;
    [TableColumn("idScheduleRulesTemplate", NotNull = true), PrimaryKey(AutoGenerated = true)]
    private int idScheduleRulesTemplate;

    [TableColumn("name", NotNull = true)]
    private string name;

    [TableColumn("rules", NotNull = true)]
    private string rules;

    [TableColumn("enabled", NotNull = true)]
    private bool enabled;

    [TableColumn("usages", NotNull = true)]
    private int  usages;

    [TableColumn("editable", NotNull = true)]
    private bool  editable;

    #endregion

    #region Constructors
    /// <summary> 
    /// Create a new object by specifying all fields (except the auto-generated primary key field). 
    /// </summary> 
    public ScheduleRulesTemplate(string name, bool enabled, int usages, bool editable)
    {
      isChanged = true;
      this.name = name;      
      this.enabled = enabled;
      this.usages = usages;
      this.editable = editable;
    }

    #endregion

    private void DeserializeRules()
    {
      try
      {
        _scheduleConditions = ScheduleConditionHelper.Deserialize<ScheduleConditionList>(rules);      
      }
      catch (Exception ex)
      {
        Log.Error("ScheduleRulesTemplate could not Deserialize Rules");
      }      
    }

    private void SerializeRules()
    {
      rules = ScheduleConditionHelper.Serialize<ScheduleConditionList>(_scheduleConditions);
    }

    #region Public Properties         

    /// <summary>
    /// Indicates whether the entity is changed and requires saving or not.
    /// </summary>
    public bool IsChanged
    {
      get { return isChanged; }
    }

    /// <summary>
    /// Property relating to database column idProgram
    /// </summary>
    public int IdProgramCategory
    {
      get { return idScheduleRulesTemplate; }
    }

    /// <summary>
    /// Property relating to database column title
    /// </summary>
    public string Name
    {
      get { return name; }
      set
      {
        isChanged |= name != value;
        name = value;
      }
    }

    public bool Enabled
    {
      get { return enabled; }
      set
      {
        isChanged |= enabled != value;
        enabled = value;
      }
    }

    public int Usages
    {
      get { return usages; }
      set
      {
        isChanged |= usages != value;
        usages = value;
      }
    }

    public bool Editable
    {
      get { return editable; }
      set
      {
        isChanged |= editable != value;
        editable = value;
      }
    }

    public ScheduleConditionList Rules
    {
      get
      {
        if (_scheduleConditions == null)
        {
          DeserializeRules();
        }
        return _scheduleConditions;        
      }
      set
      {
        _scheduleConditions = value;        
      }
    }
   
    #endregion

    #region Storage and Retrieval

    /// <summary>
    /// Static method to retrieve all instances that are stored in the database in one call
    /// </summary>
    public static IList<ScheduleRulesTemplate> ListAll()
    {
      return Broker.RetrieveList<ScheduleRulesTemplate>();
    }
   
    /// <summary>
    /// Retrieves an entity given it's id.
    /// </summary>
    public static ScheduleRulesTemplate Retrieve(int id)
    {
      // Return null if id is smaller than seed and/or increment for autokey
      if (id < 1)
      {
        return null;
      }
      Key key = new Key(typeof(ScheduleRulesTemplate), true, "idScheduleRulesTemplate", id);
      return Broker.RetrieveInstance<ScheduleRulesTemplate>(key);
    }

    /// <summary>
    /// Retrieves an entity given it's id, using Gentle.Framework.Key class.
    /// This allows retrieval based on multi-column keys.
    /// </summary>
    public static ScheduleRulesTemplate Retrieve(Key key)
    {
      return Broker.RetrieveInstance<ScheduleRulesTemplate>(key);
    }

    /// <summary>
    /// Persists the entity if it was never persisted or was changed.
    /// </summary>
    public override void Persist()
    {
      if (IsChanged || !IsPersisted)
      {
        try
        {
          SerializeRules();
          base.Persist();
        }
        catch (Exception ex)
        {
          Log.Error("Exception in ProgramCategory.Persist() with Message {0}", ex.Message);
          return;
        }
        isChanged = false;
      }
    }

    #endregion 

    public override string ToString()
    {
      return name;
    }
  }
}