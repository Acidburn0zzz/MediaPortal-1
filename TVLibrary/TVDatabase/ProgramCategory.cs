using System;
using System.Collections.Generic;
using Gentle.Framework;
using TvLibrary.Log;

namespace TvDatabase
{
  /// <summary>
  /// Instances of this class represent the properties and methods of a row in the table <b>Program</b>.
  /// </summary>
  [TableName("ProgramCategory")]
  public class ProgramCategory : Persistent
  {
    #region Members

    private bool isChanged;
    [TableColumn("idProgramCategory", NotNull = true), PrimaryKey(AutoGenerated = true)]
    private int idProgramCategory;

    [TableColumn("category", NotNull = true)]
    private string category;

    #endregion

    #region Constructors
    /// <summary> 
    /// Create a new object by specifying all fields (except the auto-generated primary key field). 
    /// </summary> 
    public ProgramCategory(string category)
    {
      isChanged = true;
      this.category = category;
    }

    #endregion

    #region Public Properties   

    /// <summary>
    /// Indicates whether the entity is changed and requires saving or not.
    /// </summary>
    public bool IsChanged
    {
      get { return isChanged; }
    }

    /// <summary>
    /// Property relating to database column idProgram
    /// </summary>
    public int IdProgramCategory
    {
      get { return idProgramCategory; }
    }

    /// <summary>
    /// Property relating to database column title
    /// </summary>
    public string Category
    {
      get { return category; }
      set
      {
        isChanged |= category != value;
        category = value;
      }
    }
   
    #endregion

    #region Storage and Retrieval

    /// <summary>
    /// Static method to retrieve all instances that are stored in the database in one call
    /// </summary>
    public static IList<ProgramCategory> ListAll()
    {
      return Broker.RetrieveList<ProgramCategory>();
    }
   
    /// <summary>
    /// Retrieves an entity given it's id.
    /// </summary>
    public static ProgramCategory Retrieve(int id)
    {
      // Return null if id is smaller than seed and/or increment for autokey
      if (id < 1)
      {
        return null;
      }
      Key key = new Key(typeof(ProgramCategory), true, "idProgramCategory", id);
      return Broker.RetrieveInstance<ProgramCategory>(key);
    }

    /// <summary>
    /// Retrieves an entity given it's id, using Gentle.Framework.Key class.
    /// This allows retrieval based on multi-column keys.
    /// </summary>
    public static ProgramCategory Retrieve(Key key)
    {
      return Broker.RetrieveInstance<ProgramCategory>(key);
    }

    /// <summary>
    /// Persists the entity if it was never persisted or was changed.
    /// </summary>
    public override void Persist()
    {
      if (IsChanged || !IsPersisted)
      {
        try
        {
          base.Persist();
        }
        catch (Exception ex)
        {
          Log.Error("Exception in ProgramCategory.Persist() with Message {0}", ex.Message);
          return;
        }
        isChanged = false;
      }
    }

    #endregion

    #region Relations

    #endregion

    public static bool CategoryExists(string s)
    {
      return true;
    }

    public override string ToString()
    {
      return category;
    }
  }
}